
package br.edu.ifro;


public class Calculadora {
    
    
    public double Multipli(double n1,double n2, double n3){
        
        return (n1*n2)*n3;
    }
    
    public double Subtrai(double n1,double n2, double n3){
        
        return (n1-n2)-n3;
    }
    public double Divisao(double n1,double n2, double n3){
        
        return (n1/n2)/n3;
    }
}
