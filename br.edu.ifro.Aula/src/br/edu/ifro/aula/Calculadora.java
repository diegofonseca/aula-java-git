public class Calculadora {
    
    public double soma (double n1, double n2){
        return n1 + n2;
    
    }
    
    public double multiplicação (double n1, double n2){
        
     return (n1 * n2);   
    
    }
    
    public double subtração (double n1, double n2){
        
     return (n1 - n2);   
          
    }
    
    public double divisão (double n1, double n2){
        
     return (n1 / n2);   
    
    }
   
}
